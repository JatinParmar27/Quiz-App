// Function to get questions by chapter
export const getNewQuestionsByChapter = (chapter: number): Question[] => {
  return allNewQuestions.filter(q => q.chapter === chapter);
};

// Function to get all available chapters from new questions
export const getNewChapters = (): number[] => {
  const chapters = [...new Set(allNewQuestions.map(q => q.chapter))];
  return chapters.sort((a, b) => a - b);
}; 